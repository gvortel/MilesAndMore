    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        long id;
        int miles;

        if( jTextField1.getText().equals("") || jTextField3.getText().equals("") ){
                popUpAllert("empty",0,0);
                return;
        }
        
        try {
            id = Long.parseLong(jTextField1.getText(), 10 );
            miles = Integer.parseInt(jTextField3.getText());

            //check if id is 10 digits long'
            int IDlength = (int) Math.log10(id) + 1;
            if( IDlength != 10 ){
                popUpAllert("input",0,0);
                return;
            }
            //check if member with ID exists
            if (!UI_FirstScreen.passengers.containsMember(id)){
                popUpAllert("member",0,0);
                return;
            }
            
            //get old miles
            int oldMiles = UI_FirstScreen.passengers.getMiles(id);
            //else insert new member
            int newMiles = UI_FirstScreen.passengers.updateMiles(id, miles);

            //Done pop up
            popUpAllert("done",oldMiles,newMiles);
            //set visible previous frame
            this.frame.setVisible(true);
            //set invisible current frame
            setVisible(false);
            //Destroy the JFrame object
            this.dispose();
        } catch (Exception e){
            popUpAllert("input",0,0);
        }

    }//GEN-LAST:event_jButton4ActionPerformed

