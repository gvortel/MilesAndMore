public void popUpAllert(String mode,int oldMiles,int newMiles)
    {
        
        if (mode.equals("empty"))
        JOptionPane.showMessageDialog(null, "Fill Member ID and Miles field!");
        
        if (mode.equals("input"))
        JOptionPane.showMessageDialog(null, "Input Error!\n"
                                                    + "Member ID:  Number 10 digits long\n");
        if (mode.equals("member"))
        JOptionPane.showMessageDialog(null, "Input Error!\n"
                                                    + "Member with ID: "+ jTextField1.getText()
                                                    + " does not exists.");
        if (mode.equals("done"))
        JOptionPane.showMessageDialog(null,jTextField3.getText()+ " miles added!"
                                                    + "Member ID: "+ jTextField1.getText()
                                                    + "\nOld miles: "+ oldMiles + ", new miles: "+newMiles+".");
    }


